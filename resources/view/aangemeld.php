<h3>Hartelijk bedankt voor het aanmelden bij een opleiding aan de Mondriaan Hogeschool</h3>

<h4>Introductie week</h4>
Om iedereen goed voor te bereiden op het nieuwe jaar hebben wij een introductieweek. Deze week duurt van maandag 27 augustus to vrijdag 31 augustus. Studenten voor de muziek opleiding worden verwacht op
maandag en studenten voor de economie opleidingen op dinsdag. Op de eerste dag krijg je verdere uitleg over hoe de week zal verlopen.

<h4>Boekenlijst</h4>
Natuurlijk heb je voor onze opleidingen boeken nodig. Tijdens de introductieweek is er de mogelijkheid om de boeken voor de desbetreffende opleiding te kopen. Omdat de boekenlijsten voor het komende jaar
nog niet definitief zijn kunnen wij op dit moment nog ggenn duidelijkheid geven over de prijzen. Zodra de boekenlijsten compleet zijn zullen deze samen met alle prijzen op onze site worden vermeld.

<h4>Studentenpas</h4>
Tijdens je studie aan de Mondriaan Hogeschool zul je gebruik maken van een studentenpas. Met deze pas kun je betalen in de schoolkantine en mediatheek, tevens ben je deze pas nodig voor tentamems.
In de introductieweek zullen er foto's worden gemaakt voor de studentenpas. Hierna ontvang je binnen twee weken je pas.